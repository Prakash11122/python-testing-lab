#1️⃣ Arithmetic Operators
a = 10
b = 3

print(a + b)   # 13
print(a - b)   # 7
print(a * b)   # 30
print(a / b)   # 3.3333
print(a % b)   # 1  (remainder)
print(a ** b)  # 1000  (power)
print(a // b)  # 3  (floor division)


#2️⃣ Comparison Operators

x = 10
y = 20

print(x == y)   # False
print(x != y)   # True
print(x > y)    # False
print(x < y)    # True
print(x >= y)   # False
print(x <= y)   # True


#3️⃣ Assignment Operators

a = 5
a += 3   # a = a + 3
print(a)  # 8

a *= 2   # a = a * 2
print(a)  # 16



#4️⃣ Logical Operators

is_logged_in = True
has_permission = False

print(is_logged_in and has_permission)  # False
print(is_logged_in or has_permission)   # True
print(not is_logged_in)                 # False


#5️⃣ Bitwise Operators

a = 5   # 0101
b = 3   # 0011

print(a & b)  # 1  (0101 & 0011 = 0001)
print(a | b)  # 7  (0101 | 0011 = 0111)
print(a ^ b)  # 6  (0101 ^ 0011 = 0110)
print(~a)     # -6 (bitwise NOT)
print(a << 1) # 10 (left shift)
print(a >> 1) # 2  (right shift)



















